    public Journal(int journalId) {
        initComponents();
        this.currentJournalId = journalId;
        JOptionPane.showMessageDialog(null, "Journal window opened with ID: " + currentJournalId);
        btnConfirm.setEnabled(false);
        
        String companyName = JournalDA.getCompanyName();
        
        if (companyName == null || companyName.trim().isEmpty()) {
            companyName = JOptionPane.showInputDialog(this, "Enter Company Name:");
            if (companyName != null && !companyName.trim().isEmpty()) {
                JournalDA.saveCompanyName(companyName);
            } else {
                companyName = "Unnamed Company";
            }
        }

        txtCompany_Name.setText(companyName);
        
        model = (DefaultTableModel) journalView.getModel();
        loadJournalData();
    }
    
