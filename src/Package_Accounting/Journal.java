     private void loadJournalData() {
        model.setRowCount(0); // clear table
        List<Object[]> entries = JournalDA.getAllJournalEntries();
        for (Object[] row : entries) {
            model.addRow(row);
        }
    }
    
    public void clear(){
        txtAcc_Title.setText("");
        txtDebit.setText("");
        txtCredit.setText("");
        btnConfirm.setEnabled(false);
    }
    
    public void validateFields(){
        String accTitle = txtAcc_Title.getText();
        int deb = Integer.parseInt(txtDebit.getText());
        int cred = Integer.parseInt(txtCredit.getText());
        if(!"".equals(accTitle) && deb >= 0 && cred >= 0)
            btnConfirm.setEnabled(true);
        else
            btnConfirm.setEnabled(false);
    }

